3 == 3
//Equal Operator
3 == '3';

//Strictly Equal
3 === '3' //False (if they were both strings, then it would come out as true)

//Not Equal
'3' != 3; //false

//Strictly Not Equal
'3' !== 3; //true

//Greater Than
3 > 2; //True

//Less Than
2 < 3; //True

//Greater Than or Equal To
4 >= 2; //True

//Less Than or Equal To
2 <= 4 //True

//And - Both statements need to be true
4 > 2 && 3 < 1; //False

//or - one can be true, one can be false?
4 > 2 || 3 < 1; //True

