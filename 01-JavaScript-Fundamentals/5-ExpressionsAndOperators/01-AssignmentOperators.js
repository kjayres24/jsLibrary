// Assignemnt operator
let r = 5;

//Addition assignment
r += 1; //r = r + 1

//Subtraction assignment
r -= 1; //r = r - 1

//Multiplication assignment
r *= 1; // r = r * 1

//Division assignment 
r /= 1; // r = r / 1

//Remainder (or Modulus) assignment 
r %= 1; // r = % 1

//Exponential assignment
r **= 1; //r = r ** 1
